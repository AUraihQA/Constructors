package Scanner;

public class Details {
	public static double addTwo(String num1, String num2) {
		return Double.parseDouble(num1) + Double.parseDouble(num2);
		
	} public static double subTwo(String num1, String num2) {
		return Double.parseDouble(num1) - Double.parseDouble(num2);
		
	}  public static double multTwo(String num1, String num2) {
		return Double.parseDouble(num1) * Double.parseDouble(num2);
		
	}  public static double divTwo(String num1, String num2) {
		return Double.parseDouble(num1) / Double.parseDouble(num2);
}
}